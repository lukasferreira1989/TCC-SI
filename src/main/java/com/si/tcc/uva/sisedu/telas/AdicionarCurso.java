    private void btConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btConfirmarActionPerformed
                   Curso curso = new Curso();
            CursoDAO dao = new CursoDAO();
        
        if (btAdicionar.isSelected()) {
     


            //aluno.setIdaluno(3);
            curso.setNome(textNome.getText());

            // String textCpfSemMasc = textCpf.getText().replaceAll("\\D", "");
       
            System.out.println(jComboBoxProfessor.getSelectedObjects());
            Professor professor = (Professor) jComboBoxProfessor.getSelectedItem();
            //System.out.println(professor.getId());
            		//System.out.println(jComboBoxProfessor.getSelectedItem().toString());
            //professor = (Professor) jComboBoxProfessor.getSelectedObjects();
            		
            
            
            
            curso.setProf(professor);
            

            try {
                dao.adicionaCurso(curso);
            } catch (SQLException ex) {
                Logger.getLogger(AdicionarCurso.class.getName()).log(Level.SEVERE, null, ex);
            }   catch (IOException ex) {
                    Logger.getLogger(AdicionarCurso.class.getName()).log(Level.SEVERE, null, ex);
                }

    
 } else if (btDeletar.isSelected()) {
                curso.setNome(textNome.getText());
                try {
                    dao.excluir(curso);
                } catch (IOException | SQLException ex) {
                    Logger.getLogger(AdicionarCurso.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
    }//GEN-LAST:event_btConfirmarActionPerformed

